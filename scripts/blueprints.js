/**
 * Generate a self-restoring crit-flash automation blueprint. Uses the light
 * "flash" parameter so lights return to their prior state automatically,
 * keeping all revert logic in Home Assistant.
 * @param {object} preset one of CRIT_BLUEPRINTS
 * @returns {string} YAML
 */
export function generateCritBlueprintYaml(preset) {
  const rgb = `[${preset.rgb.join(", ")}]`;
  return `blueprint:
  name: "HAmbience - ${preset.name}"
  description: >
    ${preset.description}
    Generated by HAmbience. The light flash is self-restoring, so your lights
    return to their previous state on their own. An optional sound can play on a
    media player. Link the automation this creates to the matching critical-roll
    preset trigger in HAmbience.
  domain: automation
  author: HAmbience
  input:
    crit_lights:
      name: Lights
      description: The lights to flash on this critical roll.
      selector:
        target:
          entity:
            - domain: light
    crit_color:
      name: Flash Color (RGB)
      default: ${rgb}
      selector:
        color_rgb: {}
    crit_sound:
      name: Sound (optional)
      description: Pick a sound file to play on a critical roll. Leave empty for no sound.
      default: ""
      selector:
        media: {}

mode: restart

trigger:
  - platform: event
    event_type: ${preset.event}

condition: []

variables:
  sound_entity_id: "{{ crit_sound.entity_id if crit_sound is mapping else '' }}"
  sound_content_id: "{{ crit_sound.media_content_id if crit_sound is mapping else '' }}"
  sound_content_type: "{{ crit_sound.media_content_type if crit_sound is mapping else 'music' }}"

action:
  # Slow, self-restoring flash so lights return to their prior state on their own.
  - service: light.turn_on
    target: !input crit_lights
    data:
      rgb_color: !input crit_color
      flash: long
  # Optional sound, only if the media selector was filled in.
  - choose:
      - conditions:
          - condition: template
            value_template: "{{ sound_entity_id != '' and sound_content_id != '' }}"
        sequence:
          - service: media_player.play_media
            target:
              entity_id: "{{ sound_entity_id }}"
            data:
              media_content_id: "{{ sound_content_id }}"
              media_content_type: "{{ sound_content_type }}"
    default: []
`;
}

