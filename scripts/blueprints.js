/**
 * Generate a valid Home Assistant *automation* blueprint for a preset.
 * The blueprint exposes light target / color / brightness / transition as
 * inputs and is triggered by a custom event, so it works natively AND can be
 * force-run via `automation.trigger` from HAmbience's webhook dispatch.
 * @param {object} preset
 * @returns {string} YAML
 */
export function generateBlueprintYaml(preset) {
  const rgb = `[${preset.rgb.join(", ")}]`;
  return `blueprint:
  name: "HAmbience - ${preset.name}"
  description: >
    ${preset.description}
    Generated by HAmbience as a starting point - customize freely with Home
    Assistant's native editors. Trigger it from HAmbience by linking the
    automation this blueprint creates to a Foundry scene.
  domain: automation
  author: HAmbience
  input:
    ambience_lights:
      name: Lights
      description: The lights this ambience controls.
      selector:
        target:
          entity:
            - domain: light
    ambience_color:
      name: Color (RGB)
      description: The mood color for this ambience.
      default: ${rgb}
      selector:
        color_rgb: {}
    ambience_brightness:
      name: Brightness
      description: 1 (dim) to 255 (full).
      default: ${preset.brightness}
      selector:
        number:
          min: 1
          max: 255
          mode: slider
    ambience_transition:
      name: Transition (seconds)
      description: Fade time when the ambience activates.
      default: ${preset.transition}
      selector:
        number:
          min: 0
          max: 30
          mode: slider

mode: restart

trigger:
  - platform: event
    event_type: hambience_activate
    event_data:
      preset: ${preset.id}

condition: []

action:
  - service: light.turn_on
    target: !input ambience_lights
    data:
      rgb_color: !input ambience_color
      brightness: !input ambience_brightness
      transition: !input ambience_transition
`;
}

