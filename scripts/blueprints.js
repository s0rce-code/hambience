/**
 * Build the HA automation that receives HAmbience service calls. A unique
 * webhook id is baked in per user so no two installs share an id.
 * @param {string} webhookId
 * @returns {string} YAML
 */
export function generateReceiverYaml(webhookId) {
  return `alias: HAmbience Webhook Receiver
description: >
  Receives service calls from the HAmbience Foundry VTT module and runs them in
  Home Assistant. Generated with a unique webhook id - keep it private.
mode: queued
max: 10
triggers:
  - trigger: webhook
    webhook_id: ${webhookId}
    allowed_methods:
      - POST
      - PUT
    local_only: false
conditions: []
actions:
  - choose:
      - conditions:
          - condition: template
            value_template: "{{ trigger.json.entity is defined }}"
        sequence:
          - action: "{{ trigger.json.service }}"
            target:
              entity_id: "{{ trigger.json.entity }}"
            data: "{{ trigger.json.data | default({}) }}"
    default:
      - action: "{{ trigger.json.service }}"
        data: "{{ trigger.json.data | default({}) }}"
`;
}
